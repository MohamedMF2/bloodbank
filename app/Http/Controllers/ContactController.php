    public function search (Request $request){
        $request ->validate([
            'query'=> 'required|min:3',
        ]);
        $query =$request->input('query');
        $contacts = Contact::where('title','like',"%$query%")
                         ->orWhere('message','like',"%$query%") 
                         ->get();
        return view('contacts.search',compact('contacts'));
